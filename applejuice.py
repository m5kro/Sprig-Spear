# Define the button to stop
button_left = [Pin(13, Pin.IN, Pin.PULL_UP), Pin(6, Pin.IN, Pin.PULL_UP)]

class applejuice:
    def __init__(self):
        self.stop_requested = False

    def setup_button_interrupts(self):
        for btn in button_left:
            btn.irq(trigger=Pin.IRQ_FALLING, handler=self.request_stop)

    def request_stop(self, pin):
        self.stop_requested = True

    async def advertise(self, payload_num, interval, loop_interval):
        ble = bluetooth.BLE()
        ble.active(True)

        if not ble.active():
            print("Failed to activate BLE")
            return

        if payload_num < 0 or payload_num >= len(payloads):
            print("Invalid payload number")
            return

        bt_data = payloads[payload_num]
        print(f"Advertising started with payload #{payload_num}...")

        try:
            while not self.stop_requested:
                print("Starting advertising...")
                ble.gap_advertise(interval, adv_data=bt_data)
                await asyncio.sleep(loop_interval)
                print("Stopping advertising...")
                ble.gap_advertise(None)
                await asyncio.sleep(1)  # Pause before restarting the advertising
        except Exception as e:
            print(f"An error occurred: {e}")
        finally:
            ble.active(False)

    async def start(self, payload_num, interval, loop_interval):
        self.setup_button_interrupts()

        # Start the advertising coroutine
        await self.advertise(payload_num, interval, loop_interval)

def startup(payload_num=0, interval=100, loop_interval=5):
    app = applejuice()
    try:
        asyncio.run(app.start(payload_num, interval, loop_interval))
    except KeyboardInterrupt:
        print("Program interrupted.")
    finally:
        print("Program terminated.")